package cn.jy.stork.biz.pojo;


public class InspectPojo {
	private int uid;
	private int rid;
	private String umemo;
	private int aid;
	private String amemo;
	private String result;
	
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getUmemo() {
		return umemo;
	}
	public void setUmemo(String umemo) {
		this.umemo = umemo;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getAmemo() {
		return amemo;
	}
	public void setAmemo(String amemo) {
		this.amemo = amemo;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	
}
